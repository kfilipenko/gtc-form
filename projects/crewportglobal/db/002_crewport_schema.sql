CREATE TABLE IF NOT EXISTS crewport.vessels (
  vessel_id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  vessel_name text NOT NULL,
  imo_number text NULL,
  call_sign text NULL,
  mmsi text NULL,
  vessel_type text NULL,
  flag_code text NULL,
  port_of_registry text NULL,
  registry_country_code text NULL,
  owner_name text NULL,
  manager_name text NULL,
  ownership_context text NULL,
  verification_state text NOT NULL DEFAULT 'pending' CHECK (verification_state IN ('pending', 'under_review', 'verified', 'rejected', 'restricted')),
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS crewport.business_client_vessels (
  business_client_vessel_id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  business_client_id uuid NOT NULL REFERENCES crewport.business_clients(business_client_id) ON DELETE CASCADE,
  vessel_id uuid NOT NULL REFERENCES crewport.vessels(vessel_id) ON DELETE CASCADE,
  relationship_type text NOT NULL CHECK (relationship_type IN ('owner', 'manager', 'operator', 'crewing_agent', 'charterer', 'other')),
  relationship_state text NOT NULL DEFAULT 'active' CHECK (relationship_state IN ('active', 'inactive', 'revoked', 'historical')),
  is_primary boolean NOT NULL DEFAULT false,
  effective_from date NULL,
  effective_to date NULL,
  notes text NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  UNIQUE (business_client_id, vessel_id, relationship_type)
);

CREATE TABLE IF NOT EXISTS crewport.vessel_documents (
  vessel_document_id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  vessel_id uuid NOT NULL REFERENCES crewport.vessels(vessel_id) ON DELETE CASCADE,
  document_type text NOT NULL,
