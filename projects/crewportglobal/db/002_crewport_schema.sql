CREATE TABLE IF NOT EXISTS crewport.crew_requests (
  crew_request_id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  business_client_id uuid NOT NULL REFERENCES crewport.business_clients(business_client_id) ON DELETE RESTRICT,
  requested_by_representative_id uuid NULL REFERENCES crewport.company_representatives(representative_id) ON DELETE SET NULL,
  vessel_id uuid NULL REFERENCES crewport.vessels(vessel_id) ON DELETE SET NULL,
  request_code text NOT NULL UNIQUE,
  title text NOT NULL,
  request_state text NOT NULL DEFAULT 'draft' CHECK (request_state IN ('draft', 'open', 'paused', 'under_review', 'fulfilled', 'cancelled', 'closed')),
  priority text NOT NULL DEFAULT 'standard' CHECK (priority IN ('low', 'standard', 'high', 'urgent')),
  needed_by_date date NULL,
  embarkation_window_start date NULL,
  embarkation_window_end date NULL,
  port_of_joining text NULL,
  notes text NULL,
  verification_gate text NOT NULL DEFAULT 'pending' CHECK (verification_gate IN ('pending', 'cleared', 'blocked')),
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS crewport.crew_request_positions (
  crew_request_position_id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  crew_request_id uuid NOT NULL REFERENCES crewport.crew_requests(crew_request_id) ON DELETE CASCADE,
  rank_code text NOT NULL,
  department_code text NOT NULL,
  quantity integer NOT NULL DEFAULT 1 CHECK (quantity > 0),
  contract_duration_months integer NULL,
  wage_currency text NULL,
  wage_amount numeric(12,2) NULL,
  minimum_experience_months integer NULL,
  status text NOT NULL DEFAULT 'open' CHECK (status IN ('open', 'shortlisting', 'offered', 'filled', 'cancelled')),
  requirement_payload jsonb NOT NULL DEFAULT '{}'::jsonb,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS crewport.candidate_matches (
  candidate_match_id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  crew_request_position_id uuid NOT NULL REFERENCES crewport.crew_request_positions(crew_request_position_id) ON DELETE CASCADE,
  seafarer_id uuid NOT NULL REFERENCES crewport.seafarers(seafarer_id) ON DELETE CASCADE,
  attempt_no integer NOT NULL CHECK (attempt_no > 0),
  is_current_attempt boolean NOT NULL DEFAULT true,
  supersedes_candidate_match_id uuid NULL REFERENCES crewport.candidate_matches(candidate_match_id) ON DELETE SET NULL,
  match_state text NOT NULL DEFAULT 'queued' CHECK (match_state IN ('queued', 'pending_review', 'shortlisted', 'presented', 'rejected', 'withdrawn', 'hired')),
  review_queue_state text NOT NULL DEFAULT 'pending' CHECK (review_queue_state IN ('pending', 'in_review', 'done', 'blocked')),
  score numeric(8,4) NULL,
  scoring_source text NULL,
  reviewer_gtc_user_id bigint NULL,
  reviewer_crewport_user_id bigint NULL,
  review_notes text NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  reviewed_at timestamptz NULL,
  closed_at timestamptz NULL,
  UNIQUE (crew_request_position_id, seafarer_id, attempt_no)
);

CREATE TABLE IF NOT EXISTS crewport.verification_events (
  verification_event_id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  subject_type text NOT NULL CHECK (subject_type IN ('physical_person', 'seafarer', 'document', 'business_client', 'business_document', 'representative', 'representative_document', 'vessel', 'vessel_document', 'business_vessel_relationship', 'crew_request', 'candidate_match', 'billing_account', 'complaint', 'consent', 'user_role')),
  subject_id uuid NOT NULL,
  event_type text NOT NULL,
  outcome text NOT NULL CHECK (outcome IN ('pending', 'passed', 'failed', 'blocked', 'waived', 'info')),
  actor_type text NOT NULL DEFAULT 'system' CHECK (actor_type IN ('system', 'crewport_user', 'gtc_operator', 'business_representative', 'seafarer')),
  actor_gtc_user_id bigint NULL,
  actor_crewport_user_id bigint NULL,
  notes text NULL,
  payload jsonb NOT NULL DEFAULT '{}'::jsonb,
  occurred_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS crewport.consent_records (
  consent_record_id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  person_id uuid NULL REFERENCES crewport.physical_persons(person_id) ON DELETE CASCADE,
  business_client_id uuid NULL REFERENCES crewport.business_clients(business_client_id) ON DELETE CASCADE,
  consent_type text NOT NULL,
  consent_version text NOT NULL,
  consent_scope text NOT NULL CHECK (consent_scope IN ('public_onboarding', 'privacy', 'matching', 'verification', 'billing', 'complaints')),
  accepted_at timestamptz NOT NULL DEFAULT now(),
  evidence_source text NOT NULL DEFAULT 'web' CHECK (evidence_source IN ('web', 'api', 'admin', 'import')),
  evidence_payload jsonb NOT NULL DEFAULT '{}'::jsonb,
  CHECK (
    (person_id IS NOT NULL AND business_client_id IS NULL)
    OR (person_id IS NULL AND business_client_id IS NOT NULL)
  )
);

CREATE TABLE IF NOT EXISTS crewport.billing_accounts (
  billing_account_id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  account_scope text NOT NULL CHECK (account_scope IN ('person', 'business_client')),
  person_id uuid NULL REFERENCES crewport.physical_persons(person_id) ON DELETE CASCADE,
  business_client_id uuid NULL REFERENCES crewport.business_clients(business_client_id) ON DELETE CASCADE,
  account_status text NOT NULL DEFAULT 'draft' CHECK (account_status IN ('draft', 'active', 'suspended', 'closed')),
  billing_email text NULL,
  invoice_currency text NOT NULL DEFAULT 'USD',
  external_processor text NULL,
  external_customer_ref text NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CHECK (
    (account_scope = 'person' AND person_id IS NOT NULL AND business_client_id IS NULL)
    OR (account_scope = 'business_client' AND person_id IS NULL AND business_client_id IS NOT NULL)
  )
);

CREATE TABLE IF NOT EXISTS crewport.service_entitlements (
  entitlement_id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  billing_account_id uuid NOT NULL REFERENCES crewport.billing_accounts(billing_account_id) ON DELETE CASCADE,
  entitlement_code text NOT NULL,
  subject_type text NOT NULL CHECK (subject_type IN ('person', 'business_client', 'crew_request', 'candidate_match', 'platform')),
  subject_id uuid NULL,
  entitlement_status text NOT NULL DEFAULT 'pending' CHECK (entitlement_status IN ('pending', 'active', 'suspended', 'expired', 'cancelled')),
  starts_at timestamptz NULL,
  ends_at timestamptz NULL,
  source_ref text NULL,
  metadata jsonb NOT NULL DEFAULT '{}'::jsonb,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS crewport.complaint_records (
  complaint_record_id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  complaint_code text NOT NULL UNIQUE,
  complaint_type text NOT NULL,
  complaint_status text NOT NULL DEFAULT 'open' CHECK (complaint_status IN ('open', 'under_review', 'resolved', 'dismissed', 'escalated', 'closed')),
  severity text NOT NULL DEFAULT 'standard' CHECK (severity IN ('low', 'standard', 'high', 'critical')),
  submitted_by_person_id uuid NULL REFERENCES crewport.physical_persons(person_id) ON DELETE SET NULL,
  submitted_by_business_client_id uuid NULL REFERENCES crewport.business_clients(business_client_id) ON DELETE SET NULL,
  related_crew_request_id uuid NULL REFERENCES crewport.crew_requests(crew_request_id) ON DELETE SET NULL,
  related_candidate_match_id uuid NULL REFERENCES crewport.candidate_matches(candidate_match_id) ON DELETE SET NULL,
  summary text NOT NULL,
  details text NULL,
  received_at timestamptz NOT NULL DEFAULT now(),
  resolved_at timestamptz NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CHECK (
    submitted_by_person_id IS NOT NULL
    OR submitted_by_business_client_id IS NOT NULL
  )
);

