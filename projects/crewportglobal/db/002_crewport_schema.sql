CREATE SCHEMA IF NOT EXISTS crewport;

CREATE OR REPLACE FUNCTION crewport.set_updated_at()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$;

CREATE TABLE IF NOT EXISTS crewport.physical_persons (
  person_id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  crewport_user_id bigint GENERATED BY DEFAULT AS IDENTITY UNIQUE,
  gtc_user_id bigint NULL,
  primary_email text NOT NULL,
  primary_email_verification_state text NOT NULL DEFAULT 'unverified' CHECK (primary_email_verification_state IN ('unverified', 'pending', 'verified', 'rejected')),
  primary_email_verified_at timestamptz NULL,
  secondary_email text NULL,
  full_name text NOT NULL,
  given_name text NULL,
  family_name text NULL,
  date_of_birth date NULL,
  nationality_code text NULL,
  residence_country_code text NULL,
  phone_e164 text NULL,
  country_code text NULL,
  preferred_locale text NOT NULL DEFAULT 'en',
  timezone_name text NULL,
  registration_source text NOT NULL DEFAULT 'crewport_public' CHECK (registration_source IN ('crewport_public', 'crewport_admin', 'business_invite', 'import', 'api')),
  onboarding_channel text NOT NULL DEFAULT 'web' CHECK (onboarding_channel IN ('web', 'admin', 'api', 'import')),
  lifecycle_state text NOT NULL DEFAULT 'draft' CHECK (lifecycle_state IN ('draft', 'active', 'suspended', 'archived')),
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS crewport.user_roles (
  user_role_id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  person_id uuid NOT NULL REFERENCES crewport.physical_persons(person_id) ON DELETE CASCADE,
  business_client_id uuid NULL,
  role_code text NOT NULL CHECK (role_code IN ('seafarer', 'business_representative', 'business_admin', 'verifier', 'reviewer', 'complaint_operator', 'billing_operator')),
  assignment_state text NOT NULL DEFAULT 'active' CHECK (assignment_state IN ('active', 'revoked')),
  granted_at timestamptz NOT NULL DEFAULT now(),
  revoked_at timestamptz NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS crewport.seafarers (
  seafarer_id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  person_id uuid NOT NULL UNIQUE REFERENCES crewport.physical_persons(person_id) ON DELETE CASCADE,
  registration_state text NOT NULL DEFAULT 'draft' CHECK (registration_state IN ('draft', 'accepted', 'under_review', 'active', 'restricted', 'archived')),
  availability_state text NOT NULL DEFAULT 'unknown' CHECK (availability_state IN ('unknown', 'available', 'committed', 'inactive')),
  rank_code text NULL,
  department_code text NULL,
  years_experience numeric(6,2) NULL,
  current_location text NULL,
  summary text NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS crewport.seafarer_documents (
  document_id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  seafarer_id uuid NOT NULL REFERENCES crewport.seafarers(seafarer_id) ON DELETE CASCADE,
  document_type text NOT NULL,
  document_number text NULL,
  issuing_country_code text NULL,
  issued_at date NULL,
  expires_at date NULL,
  verification_state text NOT NULL DEFAULT 'pending' CHECK (verification_state IN ('pending', 'received', 'under_review', 'verified', 'rejected', 'expired')),
  file_storage_key text NULL,
  metadata jsonb NOT NULL DEFAULT '{}'::jsonb,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS crewport.business_clients (
  business_client_id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  legal_name text NOT NULL,
