  registration_number text NULL,
  jurisdiction_code text NULL,
  country_code text NULL,
  company_type text NULL,
  website_url text NULL,
  primary_business_email text NULL,
  primary_phone_e164 text NULL,
  registered_address_line_1 text NULL,
  registered_address_line_2 text NULL,
  registered_city text NULL,
  registered_postal_code text NULL,
  registered_country_code text NULL,
  operating_address_line_1 text NULL,
  operating_address_line_2 text NULL,
  operating_city text NULL,
  operating_postal_code text NULL,
  operating_country_code text NULL,
  operational_role text NOT NULL,
  onboarding_state text NOT NULL DEFAULT 'draft' CHECK (onboarding_state IN ('draft', 'invited', 'submitted', 'under_review', 'active', 'restricted', 'archived')),
  verification_state text NOT NULL DEFAULT 'pending' CHECK (verification_state IN ('pending', 'under_review', 'verified', 'rejected', 'restricted')),
