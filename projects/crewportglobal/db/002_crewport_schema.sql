CREATE TABLE IF NOT EXISTS crewport.company_representatives (
  representative_id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  business_client_id uuid NOT NULL REFERENCES crewport.business_clients(business_client_id) ON DELETE CASCADE,
  person_id uuid NOT NULL REFERENCES crewport.physical_persons(person_id) ON DELETE CASCADE,
  role_title text NULL,
  authority_type text NOT NULL DEFAULT 'operator' CHECK (authority_type IN ('owner', 'director', 'authorized_signatory', 'operator', 'crewing_manager', 'observer')),
  invitation_state text NOT NULL DEFAULT 'draft' CHECK (invitation_state IN ('draft', 'invited', 'accepted', 'revoked')),
  verification_state text NOT NULL DEFAULT 'pending' CHECK (verification_state IN ('pending', 'under_review', 'verified', 'rejected')),
  is_primary boolean NOT NULL DEFAULT false,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  UNIQUE (business_client_id, person_id)
);

ALTER TABLE crewport.user_roles
  ADD CONSTRAINT fk_crewport_user_roles_business_client
  FOREIGN KEY (business_client_id) REFERENCES crewport.business_clients(business_client_id) ON DELETE CASCADE;

